    private void jEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jEditActionPerformed

        String Student_Id = jStudent_ID.getText();

        String fname =   jFirstName.getText();

        String lname =   jLastName.getText();
        
        int serial = Integer.valueOf(jSerialNo.getText());
        String gender =  "";

        if(jFemale.isSelected()){

            gender = "Female" ;
        }
        else if (jMale.isSelected()){
            
            gender = "Male" ;
        }

        if(verifyText()){

            SimpleDateFormat df = new SimpleDateFormat("YYYY-MM-DD");

            String bdate =   df.format(jBirthdate.getDate());

            String phone =   jPhone.getText();

            String email =   jEmail.getText();

            String address =  jAddress.getText();

            Student std = new Student () ;

            std.insertUpdateDelete('u', serial, Student_Id, fname, lname, gender, bdate, phone, email, address);

            MainForm.jTotal_Students.setText("Total Students = "+ Integer.toString(Count.countData("student")));

        }
    }//GEN-LAST:event_jEditActionPerformed

